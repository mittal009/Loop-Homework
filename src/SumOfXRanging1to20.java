import java.util.Scanner;

public class SumOfXRanging1to20
{
    public static void main(String[]args)
    {
        int sum = 0;
        for (int i = 1; i <= 20; i++)                //loop begins with 1 and run till i<=20
        {
            sum = sum + i;

        } System.out.println("sum: " + sum);                    //to print the sum value
    }
}
